package com.example.test_recyclerview_databiinding;

public class Memo {

	int id;
	String title;
	String memoContext;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getMemoContext() {
		return memoContext;
	}

	public void setMemoContext(String memoContext) {
		this.memoContext = memoContext;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
}
